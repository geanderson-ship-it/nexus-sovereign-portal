export const leadershipPrinciples = `
### Princípio do Líder Ensinável (O Fundamento).

Antes de tudo, o líder precisa ser ensinável. Ele não sabe tudo e nem sempre está certo. A humildade para aprender é a sua maior força. Um líder que não está aberto a novas perspectivas, que não ouve sua equipe e que não reconhece seus próprios erros cria uma cultura de medo e estagnação. A capacidade de dizer "eu não sei, vamos descobrir juntos" ou "eu errei, vamos corrigir a rota" não é fraqueza, é a mais alta demonstração de força e segurança. É essa abertura que permite a evolução contínua, tanto a sua quanto a da equipe.

### Princípio da Confiança e Empatia (O Impacto Primário).

O primeiro e mais fundamental impacto que um líder deve gerar é o de **confiança e empatia**. A empatia é a ferramenta pela qual o líder genuinamente compreende a realidade, as dificuldades e as motivações de seus colaboradores. A confiança é o resultado de ações consistentes e empáticas. Sem essa base, qualquer técnica ou estratégia se torna vazia. O primeiro contato é decisivo: um líder que se mostra disperso quebra a confiança, pois sinaliza que o colaborador não é prioridade. Um líder que reage com arrogância anula a empatia, pois sinaliza que o ambiente é perigoso. A confiança permite a vulnerabilidade e a segurança psicológica, enquanto a empatia garante que as decisões e ações do líder sejam relevantes e humanas.

### Princípio do Líder Facilitador.

O líder não resolve, ele facilita. A função do líder não é apagar incêndios ou entregar todas as respostas, mas sim destravar o caminho para que a equipe possa agir. Quando o líder resolve tudo, ele cria dependência e enfraquece o time. Quando ele facilita — orientando, removendo barreiras e dando clareza —, ele constrói autonomia, maturidade e uma equipe que cresce por si mesma. O desenvolvimento de pessoas é mais poderoso do que a entrega de soluções prontas.

### Princípio da Clareza Radical.

A principal função de um líder é ser o maior eliminador de ruído da equipe. Operar sem metas claras é operar às cegas. Um líder não deve tentar "persuadir" sua equipe a atingir um alvo que ela não pode ver. Isso gera desconfiança e desperdício de energia. A missão do líder é buscar a clareza a todo custo, mesmo que para isso precise escalar a seus próprios superiores para obter os dados. Ele absorve a pressão da ambiguidade para proteger sua equipe e só apresenta a missão quando o alvo é claro. Clareza é a base da execução, da confiança e da alta performance.

### Princípio da Responsabilidade Diferencial.

Um líder se vê diante de sua equipe como alguém que tem uma visão humana e percebe que só o que o diferencia dos demais são as suas responsabilidades. Ele não está acima, ele está à frente. Seu papel não é um privilégio, mas um encargo: a responsabilidade de desenvolver pessoas, de proteger a equipe da ambiguidade, de tomar as decisões difíceis e de criar um ambiente onde todos possam performar em seu máximo potencial. O que o define não é o poder que ele tem, mas a responsabilidade que ele assume.
`;

